private int obtenerIdTipo(Connection cn, String tipoNombre) throws SQLException {
    String select = "SELECT id_tipo FROM cat_tipo_equipo WHERE nombre = ?";
    PreparedStatement ps = cn.prepareStatement(select);
    ps.setString(1, tipoNombre);
    ResultSet rs = ps.executeQuery();

    if (rs.next()) {
        return rs.getInt(1);
    }

    // Insertar nuevo tipo
    String insert = "INSERT INTO cat_tipo_equipo(nombre) OUTPUT INSERTED.id_tipo VALUES (?)";
    ps = cn.prepareStatement(insert);
    ps.setString(1, tipoNombre);
    ResultSet rsInsert = ps.executeQuery();
    rsInsert.next();
    return rsInsert.getInt(1);
}
private int obtenerIdEstado(Connection cn, String estadoNombre) throws SQLException {
    String select = "SELECT id_estado FROM cat_estado_equipo WHERE descripcion = ?";
    PreparedStatement ps = cn.prepareStatement(select);
    ps.setString(1, estadoNombre);
    ResultSet rs = ps.executeQuery();

    if (rs.next()) {
        return rs.getInt(1);
    }

    String codigo = estadoNombre.toUpperCase().replace(" ", "_");

    String insert = "INSERT INTO cat_estado_equipo(descripcion, codigo) "
                  + "OUTPUT INSERTED.id_estado VALUES (?, ?)";
    ps = cn.prepareStatement(insert);
    ps.setString(1, estadoNombre);
    ps.setString(2, codigo);
    ResultSet rsInsert = ps.executeQuery();
    rsInsert.next();
    return rsInsert.getInt(1);
}
private int obtenerIdUbicacion(Connection cn, String ubicacionNombre) throws SQLException {
    String select = "SELECT id_ubicacion FROM ubicaciones WHERE nombre = ?";
    PreparedStatement ps = cn.prepareStatement(select);
    ps.setString(1, ubicacionNombre);
    ResultSet rs = ps.executeQuery();

    if (rs.next()) {
        return rs.getInt(1);
    }

    String codigo = ubicacionNombre.toUpperCase().replace(" ", "_");

    String insert = "INSERT INTO ubicaciones(id_tipo_ubicacion, codigo, nombre) "
                  + "OUTPUT INSERTED.id_ubicacion VALUES (?, ?, ?)";
    ps = cn.prepareStatement(insert);
    ps.setInt(1, 5);
    ps.setString(2, codigo);
    ps.setString(3, ubicacionNombre);
    ResultSet rsInsert = ps.executeQuery();
    rsInsert.next();
    return rsInsert.getInt(1);
}


