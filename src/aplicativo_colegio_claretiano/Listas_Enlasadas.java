    private void btnInsertarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsertarActionPerformed
    listaEquipos.clear();
    modeloTabla.setRowCount(0);
    String numeroSerie = txtSerie.getText().trim();
    String tipoNombre = txtTipo.getText().trim();
    String marca = txtMarca.getText().trim();
    String modelo = txtModelo.getText().trim();
    String estadoNombre = cmbEstado.getSelectedItem().toString();
    String ubicacionNombre = txtUbicacion.getText().trim();

    // Validar campos obligatorios
    if(numeroSerie.isEmpty() || tipoNombre.isEmpty() || marca.isEmpty() ||
       modelo.isEmpty() || estadoNombre.isEmpty() || ubicacionNombre.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Todos los campos son obligatorios");
        return;
    }

    try (Connection cn = Conexion.getConexion()) {

        // ️obtener o crear ID del tipo
        int idTipo = 0;
        PreparedStatement psTipo = cn.prepareStatement(
            "SELECT id_tipo FROM cat_tipo_equipo WHERE nombre = ?"
        );
        psTipo.setString(1, tipoNombre);
        ResultSet rsTipo = psTipo.executeQuery();
        if(rsTipo.next()) {
            idTipo = rsTipo.getInt(1);
        } else {
            PreparedStatement psInsertTipo = cn.prepareStatement(
                "INSERT INTO cat_tipo_equipo(nombre) OUTPUT INSERTED.id_tipo VALUES(?)"
            );
            psInsertTipo.setString(1, tipoNombre);
            ResultSet rsNuevoTipo = psInsertTipo.executeQuery();
            rsNuevoTipo.next();
            idTipo = rsNuevoTipo.getInt(1);
        }

        // Obtener o crear ID del estado
        int idEstado = 0;
        PreparedStatement psEstado = cn.prepareStatement(
            "SELECT id_estado FROM cat_estado_equipo WHERE descripcion = ?"
        );
        psEstado.setString(1, estadoNombre);
        ResultSet rsEstado = psEstado.executeQuery();
        if(rsEstado.next()) {
            idEstado = rsEstado.getInt(1);
        } else {
            PreparedStatement psInsertEstado = cn.prepareStatement(
                "INSERT INTO cat_estado_equipo(descripcion, codigo) OUTPUT INSERTED.id_estado VALUES(?, ?)"
            );
            psInsertEstado.setString(1, estadoNombre);
            psInsertEstado.setString(2, estadoNombre.toUpperCase().replace(" ", "_"));
            ResultSet rsNuevoEstado = psInsertEstado.executeQuery();
            rsNuevoEstado.next();
            idEstado = rsNuevoEstado.getInt(1);
        }

        // Obtener o crear ID de la ubicación
        int idUbicacion = 0;
        PreparedStatement psUbic = cn.prepareStatement(
            "SELECT id_ubicacion FROM ubicaciones WHERE nombre = ?"
        );
        psUbic.setString(1, ubicacionNombre);
        ResultSet rsUbic = psUbic.executeQuery();
        if(rsUbic.next()) {
            idUbicacion = rsUbic.getInt(1);
        } else {
            PreparedStatement psInsertUbic = cn.prepareStatement(
                "INSERT INTO ubicaciones(id_tipo_ubicacion, codigo, nombre) OUTPUT INSERTED.id_ubicacion VALUES(?, ?, ?)"
            );
            psInsertUbic.setInt(1, 5); 
            psInsertUbic.setString(2, ubicacionNombre.toUpperCase().replace(" ", "_"));
            psInsertUbic.setString(3, ubicacionNombre);
            ResultSet rsNuevoUbic = psInsertUbic.executeQuery();
            rsNuevoUbic.next();
            idUbicacion = rsNuevoUbic.getInt(1);
        }

        // Insertar el equipo
        String sqlInsert = "INSERT INTO equipos "
                + "(numero_serie, id_tipo, marca, modelo, id_ubicacion, id_estado, registrado_por) "
                + "OUTPUT INSERTED.id_equipo "
                + "VALUES (?, ?, ?, ?, ?, ?, ?)";

        PreparedStatement psInsert = cn.prepareStatement(sqlInsert);
        psInsert.setString(1, numeroSerie);
        psInsert.setInt(2, idTipo);
        psInsert.setString(3, marca);
        psInsert.setString(4, modelo);
        psInsert.setInt(5, idUbicacion);
        psInsert.setInt(6, idEstado);
        psInsert.setInt(7, 1);

        ResultSet rsInsert = psInsert.executeQuery();
        if(rsInsert.next()) {
            int nuevoID = rsInsert.getInt(1);
            JOptionPane.showMessageDialog(this, "Registro insertado. ID: " + nuevoID);
        }

        // Limpiar campos
        txtSerie.setText("");
        txtTipo.setText("");
        txtMarca.setText("");
        txtModelo.setText("");
        cmbEstado.setSelectedIndex(0); 
        txtUbicacion.setText("");


    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "Error al insertar: " + e.getMessage());
    }
    }//GEN-LAST:event_btnInsertarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        int fila = tblResultados.getSelectedRow();
        if(fila == -1) {
            JOptionPane.showMessageDialog(this, "Seleccione un equipo para eliminar");
            return;
        }

        // Confirmar eliminación
        int confirm = JOptionPane.showConfirmDialog(this,
                "¿Está seguro de eliminar el equipo seleccionado?",
                "Confirmar eliminación",
                JOptionPane.YES_NO_OPTION);

        if(confirm != JOptionPane.YES_OPTION) {
            return; 
        }

        //Obtener ID del equipo
        int idEquipo = (int) modeloTabla.getValueAt(fila, 0); 

        // Eliminar de la base de datos
        try (Connection cn = Conexion.getConexion()) {
            String sql = "DELETE FROM equipos WHERE id_equipo = ?";
            PreparedStatement ps = cn.prepareStatement(sql);
            ps.setInt(1, idEquipo);
            int res = ps.executeUpdate();

            if(res > 0) {
                pilaEliminados.push(listaEquipos.get(fila));
                JOptionPane.showMessageDialog(this, "Equipo eliminado correctamente");

                // Eliminar de la tabla y Listas_Enlasadas
                modeloTabla.removeRow(fila);
                listaEquipos.remove(fila);

            } else {
                JOptionPane.showMessageDialog(this, "No se pudo eliminar el equipo");
            }

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Error al eliminar: " + e.getMessage());
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

