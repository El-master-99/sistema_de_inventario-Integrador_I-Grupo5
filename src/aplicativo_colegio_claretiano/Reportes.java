private int contarEquiposRecursivo(List<Equipo> lista, int index) {
    if (index == lista.size()) return 0;
    return 1 + contarEquiposRecursivo(lista, index + 1);
}
private int contarPorMarcaRecursivo(List<Equipo> lista, int index, String marcaBuscada) {
    if (index == lista.size()) return 0;
    int suma = lista.get(index).getMarca().equalsIgnoreCase(marcaBuscada) ? 1 : 0;
    return suma + contarPorMarcaRecursivo(lista, index + 1, marcaBuscada);
}

private int contarPorEstadoRecursivo(List<Equipo> lista, int index, String estadoBuscado) {
    if (index == lista.size()) return 0;
    int suma = lista.get(index).getEstado().equalsIgnoreCase(estadoBuscado) ? 1 : 0;
    return suma + contarPorEstadoRecursivo(lista, index + 1, estadoBuscado);
}
private void generarArchivoTXT() {
    if (listaEquipos == null || listaEquipos.isEmpty()) {
        JOptionPane.showMessageDialog(this, "No hay datos para generar el reporte.");
        return;
    }

    String marcaBuscada = txtMarcaBuscar.getText().trim();
    List<Equipo> listaFiltrada;

    if (!marcaBuscada.isEmpty()) {
        listaFiltrada = listaEquipos.stream()
                .filter(e -> e.getMarca().equalsIgnoreCase(marcaBuscada))
                .toList();
    } else {
        listaFiltrada = listaEquipos;
    }

    int total = contarEquiposRecursivo(listaFiltrada, 0);
    int operativos = contarPorEstadoRecursivo(listaFiltrada, 0, "Operativo");
    int dadosBaja = contarPorEstadoRecursivo(listaFiltrada, 0, "Dado de baja");

    String ruta = System.getProperty("user.home") + "\\Desktop\\Reporte_Equipos.txt";

    try (PrintWriter writer = new PrintWriter(new FileWriter(ruta))) {

        writer.println("=======================================");
        writer.println("           REPORTE DE EQUIPOS          ");
        writer.println("=======================================");
        writer.println("Fecha: " + java.time.LocalDateTime.now());
        writer.println("---------------------------------------");
        writer.println("Total de Equipos: " + total);
        writer.println("Operativos: " + operativos);
        writer.println("Dado de Baja: " + dadosBaja);
        writer.println("---------------------------------------");

        if (!marcaBuscada.isEmpty()) {
            int totalMarca = contarPorMarcaRecursivo(listaFiltrada, 0, marcaBuscada);
            writer.println("Equipos de la marca '" + marcaBuscada + "': " + totalMarca);
            writer.println("---------------------------------------");
        }

        writer.println("\nLISTA DETALLADA:\n");

        for (Equipo e : listaFiltrada) {
            writer.println("ID: " + e.getId());
            writer.println("Número Serie: " + e.getNumeroSerie());
            writer.println("Tipo: " + e.getTipo());
            writer.println("Marca: " + e.getMarca());
            writer.println("Modelo: " + e.getModelo());
            writer.println("Estado: " + e.getEstado());
            writer.println("Ubicación: " + e.getUbicacion());
            writer.println("---------------------------------------");
        }

        JOptionPane.showMessageDialog(this, 
                "Reporte generado correctamente en:\n" + ruta);

    } catch (IOException e) {
        JOptionPane.showMessageDialog(this, 
                "Error al generar archivo: " + e.getMessage());
    }
}

