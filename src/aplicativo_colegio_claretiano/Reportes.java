    private void btnVerRegistroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnVerRegistroActionPerformed
                                                
    if (listaEquipos == null || listaEquipos.isEmpty()) {
        JOptionPane.showMessageDialog(this, "No hay equipos cargados.");
        return;
    }

    String marca = txtMarcaBuscar.getText().trim();
    List<Equipo> listaFiltrada;

    if (!marca.isEmpty()) {
        listaFiltrada = listaEquipos.stream()
                .filter(e -> e.getMarca().equalsIgnoreCase(marca))
                .toList();
    } else {
        listaFiltrada = listaEquipos;  
    }

    int total = contarEquiposRecursivo(listaFiltrada, 0);
    txtTotalEquipos.setText(String.valueOf(total));

    int operativos = contarPorEstadoRecursivo(listaFiltrada, 0, "Operativo");
    txtOperativos.setText(String.valueOf(operativos));

    int inoperativos = contarPorEstadoRecursivo(listaFiltrada, 0, "Dado de baja");
    txtInoperativos.setText(String.valueOf(inoperativos));

    if (!marca.isEmpty()) {
        txtResultadoMarca.setText(String.valueOf(total)); 
    } else {
        txtResultadoMarca.setText("0");
    }

    JOptionPane.showMessageDialog(this, "Registro generado");
    }//GEN-LAST:event_btnVerRegistroActionPerformed

